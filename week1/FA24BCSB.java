public class FA24BCSB {

    public static void main(String[] args) {

	int a = 25;
	short b = 49;
	long c = 99;
	byte d = 122;
	double  e = 91.2;
	float f = 10000.44f;
	boolean g = true;
	char h = 'c';
	String i = "Hafsa";

        System.out.printf("Value in int: %d%n", a);
        System.out.printf("Value in short: %d%n", b);
        System.out.printf("Value in long: %d%n", c);
        System.out.printf("Value in byte: %d%n", d);
        System.out.printf("Value in double: %.2f%n", e);
        System.out.printf("Value in float: %,.2f  %n", f);
        System.out.printf("Value in boolean: %b%n", g);
        System.out.printf("Value in char: %c%n", h);
	System.out.printf("Value in string: %s%n", i);
    }
}
